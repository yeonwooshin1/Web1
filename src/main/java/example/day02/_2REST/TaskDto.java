package example.day02._2REST;   // package

public class TaskDto {  // class start
    private int age;
    private String name;

    public TaskDto(int age, String name) {
        this.age = age;
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}   // class end
